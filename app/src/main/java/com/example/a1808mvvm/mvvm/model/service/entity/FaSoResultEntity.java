package com.example.a1808mvvm.mvvm.model.service.entity;

public class FaSoResultEntity {

    /**
     * code : 200
     * data : 发布头条成功
     * msg : 操作成功
     */

    private int code;
    private String data;
    private String msg;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
